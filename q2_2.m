N=1000;
half_len=10;
figure(1);
x1=@(t)(1.5*exp(-2*t).*heaviside(t)+1.5*exp(3*t).*heaviside(-t));
x2=@(t)(cos(2*t).*(heaviside(t)-heaviside(t-2)));
t=linspace(-half_len, half_len, N);
subplot(311);
plot(t, x1(t));
title('x1(t)');
grid on;
subplot(312);
plot(t, x2(t));
title('x2(t)');
grid on;
subplot(313);
plot(linspace(-2*half_len, 2*half_len, 2*N-1), conv(x1(t), x2(t))*(2*half_len/(N-1)));
title('y(t)=x1(t)*x2(t)');
grid on;

figure(2);
N=-10:10;
x3=@(n)((-2/3).^n).*heaviside(n-1);
x4=@(n)((-1).^(n+1).*heaviside(n+1)-(-2).^(n-2).*heaviside(n-2));
subplot(131);
stem(N, x3(N), '.');
title('x3[n]');
subplot(132);
stem(N, x4(N), '.');
title('x4[n]');
subplot(133);
stem(-length(N)+1:length(N)-1, conv(x3(N), x4(N)), '.');
title('y[n]=x3[n]*x4[n]');
